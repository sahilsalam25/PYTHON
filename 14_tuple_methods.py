# 1-- count- it count the no of elements
t=[1,4,6,2,8,1,2,10,4,1]
print(t) # printing a tuple
print(t.count(2)) # it will count no of occurence of 2 in the given tuple ---t=[1,4,6,2,8,1,2,10,4,1]
 # 2--index , it is used to find the exact of index of an elements

print(t.index(4)) # element 4 is present at the index 1 in the given tuple --- t=[1,4,6,2,8,1,2,10,4,1]
 
