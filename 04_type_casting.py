# type casting is a method to change a datatype to another datatype...ex. a="10", a=int(a)
# output =10
# String to Integer Conversion :
a="10" # this is string literal
a=int(a) # but here we are type casting from string to integer using int function
print(type(a))
print(a)
# Integer to String Conversion :
b=15 # this is a integer value
b=str(b) # but we are type casting from integer to string literals :
print(type(b))
print(b)