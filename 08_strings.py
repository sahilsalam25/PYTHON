# Slicing the String
name="My Name i SAHIL SALAM"
print(name[0:4]) # this will print the value from 0 to 4 i.e my n
print(name[1:8]) # this will print the value from 0 to 4 i.e my n
print(name[:4]) # this will print the value same as [0:4]
print(name[10]) # printing the word at no=10
# Slicing with skip value
word="HPLaptop"
print(word[1:8:2]) # this will print the value with skip value 2 and print under only 8 value
print(word[1:10:3]) # this will print the value with skip value 3 and take total 10 letter from output
